package java_concept;

public class Switch
{
	public static void main(String[] args)
	{
		int key = 6;
		
		switch (key) 
		{
		case 1:
		{
			
           System.out.println("mon");
           break;
		}
		case 2:
		{
			
           System.out.println("tue");
           break;
		}
		case 3:
		{
			
           System.out.println("thus");
           break;
		}
		default:
			System.out.println("Looking forward to the Weekend");
		}
		
	}

}
