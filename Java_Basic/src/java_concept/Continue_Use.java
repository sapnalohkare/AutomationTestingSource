package java_concept;

public class Continue_Use 
{
	public static void main(String[] args) 
	{
		int i = 0;
		while (i < 10) {
		 
			if (i <= 4) 
			{
				
			    System.out.println(i);
			    i++;
			    
			    continue;
		   
			}
			else
			{ 
				  	break;
				  
			}
		 // System.out.println(i);
		  
		  
		}
	}

}
