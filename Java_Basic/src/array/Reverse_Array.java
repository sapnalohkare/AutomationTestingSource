package array;

public class Reverse_Array {

	public static void main(String[] args) 
	{

     int[] arr= new int[3];
     arr[0]= 400;
     arr[1]= 69;
     arr[2]= 2;
     
     
     //reversal of an arrray
     for (int i = (arr.length-1); i >= 0; i--) 
     {
		System.out.println(arr[i]);
	  }
     

	}

}
